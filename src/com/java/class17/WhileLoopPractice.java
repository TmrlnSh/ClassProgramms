package com.java.class17;

public class WhileLoopPractice {
    public static void main(String[] args) {
        int num1 = 0;
        int num2 = 11;
        while(num1<=0){
            num1++;
            num2--;
            System.out.println(num1);
            System.out.println(num2);
        }
    }
}